void Servo_SetAngle(uint8_t angle) {

  if (angle > 180) angle = 180;
  if (angle < 0) angle = 0;
  

  uint32_t pulse = 500 + (angle * 2000 / 180);
  TIM2->CCR1 = pulse;  
}
